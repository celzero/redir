ON playorders(linkedtoken);

CREATE INDEX IF NOT EXISTS idx_playorders_cid
ON playorders(cid);

CREATE INDEX IF NOT EXISTS idx_devices_cid
ON devices(cid);

CREATE INDEX IF NOT EXISTS idx_wslease_cid
ON wslease(cid);

CREATE INDEX IF NOT EXISTS idx_wsperma_cid
ON wsperma(cid);

